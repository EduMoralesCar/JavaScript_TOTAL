// Variables en JavaScript
console.log("Variables en JavaScript");

let producto = "Monitor 20 Pulgadas";
console.log(producto);

producto = "Monitor 24 Pulgadas"; // Reasignamos el valor de la variable
console.log(producto);

producto = 20; // Cambiando el tipo de dato a número
producto = true; // Cambiando el tipo de dato a booleano
producto = null; // Cambiando el tipo de dato a null
console.log(producto);

console.log("Declaración de una variable con valor");
let precio; // Declaración de una variable sin valor
precio = 29.99; // Asignación de un valor numérico
console.log(precio);