//Funciones, métodos y propiedades matemáticas

// Propiedades Matemáticas
console.log('Propiedades Matemáticas:');
console.log('PI:', Math.PI);
console.log('E:', Math.E);

// Métodos Matemáticos
console.log('Métodos Matemáticos:');
console.log('Redondear:', Math.round(4.7)); // Devuelve 5
console.log('Redondear hacia Arriba:', Math.ceil(4.1)); // Devuelve 5
console.log('Redondear hacia Abajo:', Math.floor(4.9)); // Devuelve 4
console.log('Absoluto:', Math.abs(-5)); // Devuelve 5
console.log('Raíz Cuadrada:', Math.sqrt(16)); // Devuelve 4
console.log('Potencia:', Math.pow(2, 3)); // Devuelve 8
console.log('Número Aleatorio:', Math.random()); // Devuelve un número aleatorio entre 0 y 1
console.log('Seno:', Math.sin(45)); // Devuelve 0.8509035245341184
console.log('Coseno:', Math.cos(45)); // Devuelve 0.5253219888177297
console.log('Tangente:', Math.tan(45)); // Devuelve 1.6197751905438615
