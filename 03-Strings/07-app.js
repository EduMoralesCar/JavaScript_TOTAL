// Ejemplo de uso de métodos de cadenas en JavaScript de tolowerCase, toUpperCase y toString
console.log();
const pelicula = "El Arbol de la Vida";
console.log(pelicula.toLowerCase()); // Convierte la cadena a minúsculas
console.log(pelicula.toUpperCase()); // Convierte la cadena a mayúsculas

const cantidad = 1000;
console.log(cantidad);
console.log(cantidad.toString()); // Convierte el número a cadena de texto

console.log(pelicula.toString()); // Convierte la cadena a cadena (redundante, pero válido)