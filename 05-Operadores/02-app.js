// Operadores de Comparación en JavaScript
// Veamos cómo comparar valores utilizando operadores de comparación
const valor1 = 10;
const valor2 = "10";
const valor3 = 15;

// Revisar si 2 numeros son iguales
console.log();
console.log("---------------------------------------------");
console.log("Comparando Valores con Operadores:");
console.log("---------------------------------------------");
console.log(valor1 == valor2); // true (comparación de igualdad)
console.log(valor1 === valor2); // false (comparación estricta de igualdad)
console.log(valor1 != valor2); // true (comparación de desigualdad)
console.log(valor1 !== valor2); // true (comparación estricta de desigualdad)
