// Operadores de Comparación en JavaScript
// Veamos cómo comparar valores utilizando operadores de comparación
const valor1 = 5;
const valor2 = "10";
const valor3 = 15;

// Imprimir las comparaciones
console.log();
console.log("---------------------------------------------");
console.log("Comparando Valores con Operadores:");
console.log("---------------------------------------------");
console.log(valor1 > valor2); // false
console.log(valor2 > valor1); // true
console.log(valor3 >= valor1); // true
console.log(valor1 < valor3); // true
console.log(valor2 <= valor3); // true